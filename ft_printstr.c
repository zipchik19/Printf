#include "ft_printf.h"

int	ft_printstr(char *s)
{
	int	i;

	i = 0;
	while (s == NULL)
	{
		ft_putstr("(null)");
		return (6);
	}
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
